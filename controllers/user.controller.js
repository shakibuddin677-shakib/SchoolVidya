// ================== UPLOAD / UPDATE AVATAR ==================
// req.file yahan se aata hai - upload.middleware.js (multer) isko
// route pe chalne ke baad bhar deta hai
export const uploadAvatar = async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({ success: false, message: "No image file uploaded" });
    }

    // BUG FIX: this route allows role "student"/"teacher" (so people can update
    // their own avatar), but it never checked that req.params.id was actually
    // THEIR id. Without this check, any logged-in student/teacher could pass
    // any other user's id in the URL and overwrite (and delete) that user's
    // avatar. Admins are still allowed to update anyone's avatar.
    if (req.user.role !== "admin" && req.user._id.toString() !== req.params.id) {
      return res.status(403).json({
        success: false,
        message: "You can only update your own avatar",
      });
    }

    const user = await User.findById(req.params.id);
    if (!user) {
      return res.status(404).json({ success: false, message: "User not found" });
    }

    // Agar user ka pehle se avatar tha, purana Cloudinary se delete kar do
    // (warna Cloudinary account mein bekar files jamaa hoti rahengi)
    if (user.avatar?.public_id) {
      await deleteFromCloudinary(user.avatar.public_id);
    }

    // req.file.buffer - yeh raw image data hai (RAM mein), multer ne bhara
    const result = await uploadToCloudinary(req.file.buffer, "avatars");

    user.avatar = {
      public_id: result.public_id, // baad mein delete karne ke liye reference
      url: result.secure_url, // yeh URL frontend <img> tag mein use hoga
    };
    await user.save();

    return res.status(200).json({
      success: true,
      message: "Avatar uploaded successfully",
      data: user.avatar,
    });
  } catch (error) {
    return res.status(500).json({ success: false, message: error.message });
  }
};

// ================== CREATE STUDENT ==================
// Ek hi request mein: User (login) + Parent (agar naya) + Student (profile) - teeno banenge
// "password" ab OPTIONAL hai - agar Admin nahi deta, system khud bana kar email kar deta hai
export const createStudent = async (req, res) => {
  let createdUser = null; // isko track karenge taaki error aane par rollback kar sakein

  try {
    const {
      name, email, password, // User ke liye
      rollNo, admissionNo, dob, gender, classId, sectionId, bloodGroup, address,
      parent, // { name, phone, email, occupation } - optional naya parent object
    } = req.body;

    if (!name || !email || !rollNo || !admissionNo || !classId || !sectionId) {
      return res.status(400).json({ success: false, message: "Required fields missing" });
    }

    // Agar Admin ne password nahi diya, khud generate karo (10-character random string)
    const isAutoGenerated = !password;
    const plainPassword = password || generateTempPassword();

    // STEP 1: Login account banao
    const hashedPassword = await bcrypt.hash(plainPassword, 10);
    createdUser = await User.create({ name, email, password: hashedPassword, role: "student" });

    // STEP 2: Agar parent details di hain, Parent record bhi bana do
    let parentId = null;
    if (parent && parent.name && parent.phone) {
      const newParent = await Parent.create(parent);
      parentId = newParent._id;
    }

    // STEP 3: Student profile banao, User ke saath link karke
    const newStudent = await Student.create({
      userId: createdUser._id,
      rollNo, admissionNo, dob, gender, classId, sectionId, parentId, bloodGroup, address,
    });

    // STEP 4: Agar password auto-generate hua tha, credentials email kar do
    // (agar Admin ne khud password set kiya, matlab wo khud hi student ko batayega)
    if (isAutoGenerated) {
      const html = credentialsEmailTemplate({
        name,
        email,
        password: plainPassword,
        role: "Student",
        loginUrl: `${process.env.FRONTEND_URL}/login`,
      });

      // Email fail bhi ho jaye to poori request fail nahi honi chahiye -
      // account to ban chuka hai, sirf email delivery ek "best-effort" hai
      try {
        await sendEmail({ email, subject: "Your EduCore Account Credentials", html });
      } catch (emailError) {
        console.error("Credentials email failed to send:", emailError.message);
      }
    }

    return res.status(201).json({
      success: true,
      message: isAutoGenerated
        ? "Student created successfully. Login credentials emailed to the student."
        : "Student created successfully",
      data: { user: createdUser, student: newStudent },
    });
  } catch (error) {
    // ROLLBACK: agar beech mein fail hua, adhoora User delete kar do
    if (createdUser) await User.findByIdAndDelete(createdUser._id);
    return res.status(500).json({ success: false, message: error.message });
  }
};

// ================== CREATE TEACHER ==================
