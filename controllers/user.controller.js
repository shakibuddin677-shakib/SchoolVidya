// ================== GET ALL USERS (role se filter, profile ke saath) ==================
// Example: GET /api/users?role=student&page=1&limit=10
export const getAllUsers = async (req, res) => {
  try {
    const { role, classId, sectionId } = req.query;
    const page = Number(req.query.page) || 1;
    const limit = Number(req.query.limit) || 10;

    let filter = role ? { role } : {};

    // Agar Student list ko Class/Section se filter karna hai (jaise
    // Attendance mark karte waqt "is Section ke students dikhao"),
    // pehle Student profiles se matching userIds nikalo, phir User
    // query ko un IDs tak seemit karo
    if (role === "student" && (classId || sectionId)) {
      const studentFilter = {};
      if (classId) studentFilter.classId = classId;
      if (sectionId) studentFilter.sectionId = sectionId;

      const matchingStudents = await Student.find(studentFilter).select("userId");
      const userIds = matchingStudents.map((s) => s.userId);
      filter._id = { $in: userIds };
    }

    const totalUsers = await User.countDocuments(filter);
    const users = await User.find(filter)
      .sort({ createdAt: -1 })
      .skip((page - 1) * limit)
      .limit(limit);

    // Har User ke saath uska profile (Student ya Teacher) bhi jodna hai
    // Promise.all() isliye kyunki hume har user ke liye ek ALAG DB query
    // karni hai, aur hum chahte hain ki sab EK SAATH (parallel) chalein,
    // ek-ek karke nahi (warna 20 users ke liye bahut slow ho jayega)
    const usersWithProfile = await Promise.all(
      users.map(async (user) => {
        let profile = null;
        if (user.role === "student") {
          profile = await Student.findOne({ userId: user._id })
            .populate("classId", "name")
            .populate("sectionId", "name");
        } else if (user.role === "teacher") {
          // Subject ke saath uski class bhi populate karo - Subject model
          // class-scoped hai (jaise "Mathematics" Class 5 aur Class 8 ke
          // liye alag documents), sirf naam dikhane se same-naam wale
          // subjects duplicate jaise lagte the
          profile = await Teacher.findOne({ userId: user._id }).populate({
            path: "subjects",
            select: "name classId",
            populate: { path: "classId", select: "name" },
          });
        }
        return { ...user.toObject(), profile };
      })
    );

    return res.status(200).json({
      success: true,
      data: usersWithProfile,
      pagination: { total: totalUsers, page, totalPages: Math.ceil(totalUsers / limit) },
    });
  } catch (error) {
    return res.status(500).json({ success: false, message: error.message });
  }
};

// ================== GET SINGLE USER (with profile) ==================
export const getUserById = async (req, res) => {
  try {
    const user = await User.findById(req.params.id);
    if (!user) return res.status(404).json({ success: false, message: "User not found" });

    let profile = null;
    if (user.role === "student") {
      profile = await Student.findOne({ userId: user._id })
        .populate("classId", "name")
        .populate("sectionId", "name")
        .populate("parentId");
    } else if (user.role === "teacher") {
      profile = await Teacher.findOne({ userId: user._id }).populate({
        path: "subjects",
        select: "name classId",
        populate: { path: "classId", select: "name" },
      });
    }

    return res.status(200).json({ success: true, data: { user, profile } });
  } catch (error) {
    return res.status(500).json({ success: false, message: error.message });
  }
};

// ================== UPDATE USER ==================
// User (name/email) aur uska profile (rollNo/classId/etc.) dono ek saath update
export const updateUser = async (req, res) => {
  try {
    const { name, email, ...profileUpdates } = req.body; // "...profileUpdates" = baaki sab fields

    const user = await User.findById(req.params.id);
    if (!user) return res.status(404).json({ success: false, message: "User not found" });

    if (name) user.name = name;
    if (email) user.email = email;
    await user.save();

    let updatedProfile = null;
    if (user.role === "student") {
      updatedProfile = await Student.findOneAndUpdate({ userId: user._id }, profileUpdates, {
        new: true,
        runValidators: true,
      });
    } else if (user.role === "teacher") {
      updatedProfile = await Teacher.findOneAndUpdate({ userId: user._id }, profileUpdates, {
        new: true,
        runValidators: true,
      });
    }

    return res.status(200).json({
      success: true,
      message: "User updated successfully",
      data: { user, profile: updatedProfile },
    });
  } catch (error) {
    return res.status(500).json({ success: false, message: error.message });
  }
};

// ================== DELETE USER ==================
export const deleteUser = async (req, res) => {
  try {
    const user = await User.findById(req.params.id);
    if (!user) return res.status(404).json({ success: false, message: "User not found" });

    if (user.role === "student") {
      const studentProfile = await Student.findOne({ userId: user._id });

      if (studentProfile) {
        // CASCADE DELETE: is Student ki saari related records bhi saaf karo,
        // warna yeh "orphaned" documents ban jaate hain - jo baad mein
        // Reports (Fee Collection, Exam Performance) mein galat numbers
        // deते hain (jaise negative "pending" amount)
        await Promise.all([
          Attendance.deleteMany({ studentId: studentProfile._id }),
          Result.deleteMany({ studentId: studentProfile._id }),
          FeePayment.deleteMany({ studentId: studentProfile._id }),
          HomeworkSubmission.deleteMany({ studentId: studentProfile._id }),
          BookIssue.deleteMany({ studentId: studentProfile._id }),
        ]);
      }

      await Student.findOneAndDelete({ userId: user._id });
    } else if (user.role === "teacher") {
      const teacherProfile = await Teacher.findOne({ userId: user._id });

      if (teacherProfile) {
        // Teacher delete hone par uske timetable periods bhi hata do,
        // aur agar kisi Section ka classTeacher yehi tha, wo bhi clear karo
        await Timetable.deleteMany({ teacherId: teacherProfile._id });
        // classTeacherId references the teacher's User _id (so it can populate
        // name/email from the User collection) - NOT the Teacher profile's own _id
        await Section.updateMany({ classTeacherId: user._id }, { classTeacherId: null });
      }

      await Teacher.findOneAndDelete({ userId: user._id });
    }

    await User.findByIdAndDelete(user._id);

    return res.status(200).json({ success: true, message: "User deleted successfully" });
  } catch (error) {
    return res.status(500).json({ success: false, message: error.message });
  }
};
